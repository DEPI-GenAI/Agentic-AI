def repair_code(question: str, schema: dict, code: str, issues: list) -> str:
  """Use local Phi-3.5 to repair code based on validation issues"""
  issues_str = "\n".join([f"- {issue}" for issue in issues])

  repair_prompt = f"""You are a pandas expert. Fix the code based on the validation feedback.

  Question: {question}

  Schema:
  {json.dumps(schema, indent=2)}

  Previous code:
  ```python
  {code}
  Validation failed with these issues:
  {issues_str}
  Fix the code so it correctly answers the question.
  Rules:

  Return ONLY the corrected Python code
  No explanations
  No markdown
  Must assign the final answer to variable result
  Use proper groupby/aggregation when needed

  Fixed code:
  """
  try:
    fixed = ask_llm(repair_prompt, max_tokens=400)

    #Clean the output
    cleaned = clean_llm_code(fixed)
    print(f"[REPAIR] Generated fixed code")
    return cleaned if cleaned else fixed

  except Exception as e:
    print(f"[REPAIR] Error: {e}")
    return code  # fallback to original

def execute_with_validation(question: str, df: pd.DataFrame, max_validation_retries: int = 3):
    schema = {
        "columns": list(df.columns),
        "dtypes": {col: str(dtype) for col, dtype in df.dtypes.items()}
    }

    print(f"\n=== Solving: {question} ===")

    # Generate
    code = ask_llm(build_prompt(question, schema))
    print(f"[GEN] {code.strip()}")

    validation_attempts = 0
    max_val_retries = max_validation_retries

    while validation_attempts < max_val_retries:
        validation = validate_code(question, schema, code)

        if validation.get("is_valid", False):
            print("[VALIDATION] PASSED ✓")
            break

        print(f"[REPAIR] Attempt {validation_attempts + 1}")
        code = repair_code(question, schema, code, validation.get("issues", []))
        validation_attempts += 1

    if not validation.get("is_valid", False):
        print("[FINAL] Validation failed after retries")
        return "I am not confident this code answers correctly. Try rephrasing."

    # Execute
    print("[EXECUTION] Running code safely...")
    try:
        result = run_code(code, df)
        print("✅ Execution successful")

        # Nicer output for pandas objects
        if isinstance(result, pd.Series) or isinstance(result, pd.DataFrame):
            print(result)
        else:
            print(result)

        return result
    except Exception as e:
        print(f"Runtime Error: {e}")
        return f"Execution failed: {str(e)}"

