GRACEFUL_FAILURE_MSG = "I could not compute this safely. Try rephrasing."

def execute_with_retry(
    question: str,
    df: pd.DataFrame,
    max_retries: int = 5,
) -> Any:
    """
    Level-2 agent loop:
      1. Policy check → reject if unsafe
      2. Generate code (Phi-3.5)
      3. Try to execute in sandbox
         ✓ success  → return result
         ✗ failure  → build repair prompt → ask LLM to fix → retry
      4. If all retries exhausted → return GRACEFUL_FAILURE_MSG (never crash)

    Logs each step to stdout for traceability.
    """
    print(f"\n{'='*60}")
    print(f"[QUESTION] {question}")
    print(f"{'='*60}")

    # ── 1. Policy check ────────────────────────────────────────────────────────
    if is_rejected_by_policy(question):
        print("[POLICY] ❌ Rejected — violates aggregation-only policy")
        return "Policy violation: only aggregated analytics queries are allowed."

    # ── 2. Build schema ────────────────────────────────────────────────────────
    schema = {
        "columns": list(df.columns),
        "dtypes":  {col: str(dtype) for col, dtype in df.dtypes.items()},
        "rows":    len(df),
    }

    # ── 3. Initial code generation ────────────────────────────────────────────
    code = ask_llm(build_prompt(question, schema))
    print(f"[GEN] Initial code:\n{code}\n")

    # ── 4. Retry loop ─────────────────────────────────────────────────────────
    for attempt in range(1, max_retries + 1):
        print(f"[ATTEMPT {attempt}/{max_retries}]")
        try:
            result = run_code(code, df)
            print(f"[SUCCESS] ✅ Execution succeeded on attempt {attempt}")
            return result

        except Exception as exc:
            error_str = str(exc)
            print(f"[FAILURE] ❌ {type(exc).__name__}: {error_str}")

            if attempt == max_retries:
                # All retries exhausted — graceful failure
                print("[GRACEFUL FAILURE] Max retries reached. Returning safe message.")
                return GRACEFUL_FAILURE_MSG

            # Ask LLM to repair
            print(f"[REPAIR] Sending error back to LLM for fix …")
            fix_prompt = repair_prompt(schema, question, code, error_str)
            code = ask_llm(fix_prompt, max_tokens=350)
            print(f"[REPAIR] Repaired code:\n{code}\n")

    # Should never reach here, but be safe
    return GRACEFUL_FAILURE_MSG

